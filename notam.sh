# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –≤–∞–ª–∏–¥–Ω–æ—Å—Ç–∏ –∫–æ–¥–∞ ICAO
is_valid_icao() {
    local icao=$1
    [[ ${#icao} -eq 4 ]] && [[ "$icao" =~ ^[A-Z]{4}$ ]]
}

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è NOTAM –∏–∑ –ø—É–±–ª–∏—á–Ω–æ–≥–æ API (–æ–≥—Ä–∞–Ω–∏—á–µ–Ω–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏–æ–Ω–∞–ª—å–Ω–æ—Å—Ç—å)
fetch_notam_public() {
    local icao=$1
    local notam_data=""
    local http_code=0
    
    echo -e "${CYAN}üîç –ü–æ–∏—Å–∫ NOTAM –¥–ª—è $icao...${NC}" >&2
    
    # –ò—Å—Ç–æ—á–Ω–∏–∫ 1: AviationAPI (–æ–≥—Ä–∞–Ω–∏—á–µ–Ω–Ω—ã–π –±–µ—Å–ø–ª–∞—Ç–Ω—ã–π –¥–æ—Å—Ç—É–ø)
    echo -e "${WHITE}–ü–æ–ø—ã—Ç–∫–∞ 1: AviationAPI...${NC}" >&2
    notam_data=$(curl -s --connect-timeout 10 --max-time 15 -w "%{http_code}" \
        "https://api.aviationapi.com/v1/notams/apt?apt=$icao" 2>/dev/null)
    
    # –ò–∑–≤–ª–µ–∫–∞–µ–º HTTP-–∫–æ–¥ (–ø–æ—Å–ª–µ–¥–Ω–∏–µ 3 —Å–∏–º–≤–æ–ª–∞)
    http_code=${notam_data: -3}
    # –ò–∑–≤–ª–µ–∫–∞–µ–º —Ç–µ–ª–æ –æ—Ç–≤–µ—Ç–∞ (–≤—Å–µ, –∫—Ä–æ–º–µ –ø–æ—Å–ª–µ–¥–Ω–∏—Ö 3 —Å–∏–º–≤–æ–ª–æ–≤)
    notam_data=${notam_data%???}
    
    # –£—Å–ø–µ—Ö, –µ—Å–ª–∏ –∫–æ–¥ 200 –∏ –¥–∞–Ω–Ω—ã–µ –Ω–µ –ø—É—Å—Ç—ã –∏ –Ω–µ —Å–æ–¥–µ—Ä–∂–∞—Ç –æ—à–∏–±–∫—É
    if [[ $http_code -eq 200 ]] && [[ -n "$notam_data" ]] && [[ "$notam_data" != *"error"* ]] && [[ "$notam_data" != *"null"* ]]; then
        echo -e "${GREEN}–î–∞–Ω–Ω—ã–µ –ø–æ–ª—É—á–µ–Ω—ã –æ—Ç AviationAPI.${NC}" >&2
        echo "$notam_data"
        return 0
    fi
    
    # –ò—Å—Ç–æ—á–Ω–∏–∫ 2: –ó–∞–ø–∞—Å–Ω–æ–π –≤–∞—Ä–∏–∞–Ω—Ç - AviationWeather (–ø—Ä–æ–±—É–µ–º –¥—Ä—É–≥–æ–π —Ñ–æ—Ä–º–∞—Ç)
    echo -e "${YELLOW}–ü–æ–ø—ã—Ç–∫–∞ 2: AviationWeather...${NC}" >&2
    notam_data=$(curl -s --connect-timeout 10 --max-time 15 \
        "https://aviationweather.gov/cgi-bin/data/notams.php?format=json&ids=$icao" 2>/dev/null)
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –¥–∞–Ω–Ω—ã–µ –µ—Å—Ç—å –∏ –æ–Ω–∏ –Ω–µ –ø—É—Å—Ç—ã–µ
    if [[ -n "$notam_data" ]] && [[ "$notam_data" != *"error"* ]] && [[ "$notam_data" != "[]" ]]; then
        echo -e "${GREEN}–î–∞–Ω–Ω—ã–µ –ø–æ–ª—É—á–µ–Ω—ã –æ—Ç AviationWeather.${NC}" >&2
        echo "$notam_data"
        return 0
    fi
    
    # –ï—Å–ª–∏ –æ–±–∞ –∏—Å—Ç–æ—á–Ω–∏–∫–∞ –Ω–µ —Å—Ä–∞–±–æ—Ç–∞–ª–∏
    echo -e "${RED}–ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –¥–∞–Ω–Ω—ã–µ NOTAM –∏–∑ –ø—É–±–ª–∏—á–Ω—ã—Ö –∏—Å—Ç–æ—á–Ω–∏–∫–æ–≤.${NC}" >&2
    return 1
}

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–∞—Ä—Å–∏–Ω–≥–∞ NOTAM –∏–∑ JSON
parse_notam_json() {
    local json_data=$1
    local icao=$2
    
    if command -v jq &> /dev/null; then
        echo "$json_data" | jq -r '.[] | "\(.Number // .id // "N/A"): \(.Message // .message // .text // "N/A")\n–ü–µ—Ä–∏–æ–¥: \(.StartTime // .startTime // "N/A") - \(.EndTime // .endTime // "N/A")\n"' 2>/dev/null
    else
        # –£–ª—É—á—à–µ–Ω–Ω—ã–π –ø–∞—Ä—Å–∏–Ω–≥ –±–µ–∑ jq (–±–æ–ª–µ–µ —É—Å—Ç–æ–π—á–∏–≤—ã–π, –Ω–æ –≤—Å–µ –∂–µ –Ω–µ–Ω–∞–¥–µ–∂–Ω—ã–π)
        # –í—ã–≤–æ–¥–∏–º –≤—Å–µ, —á—Ç–æ –ø–æ—Ö–æ–∂–µ –Ω–∞ —Ç–µ–∫—Å—Ç NOTAM, —Å—Ç–∞—Ä–∞—è—Å—å –æ—Ç—Ñ–∏–ª—å—Ç—Ä–æ–≤–∞—Ç—å –º—É—Å–æ—Ä
        echo "$json_data" | grep -oE '" (Message|message|text)"?[ :]*"[^"]*"' | sed 's/"Message":"//g; s/"message":"//g; s/"text":"//g; s/"//g' | head -5
    fi
}

